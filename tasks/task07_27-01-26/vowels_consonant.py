# W.A.P to check vowels or consonant by taking a character

vowels = (input("enter Vowel: "))
match vowels:
    case "a":
        print("vowel")
    case "e":
        print("Vowel")
    case "i":
        print("Vowel")
    case "o":
        print("Vowel")
    case "u":
        print("Vowel")
    case _:
        print("Consonant")