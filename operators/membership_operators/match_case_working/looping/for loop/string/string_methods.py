
# class str:
#     def capitalize(self) => return a capitalized version of the string.
#     def casefold(self) => lowercase.
#     def index(self,substr) => return index of substr.

name = "Luminar Technolab"
print(name.capitalize())
print(name.casefold())
print(name.index("ch"))
print(name.index("Luminar Technolab"))
print(name.count("x"))
