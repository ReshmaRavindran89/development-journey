day = int(input("Enter the Number: ")) # 2
if day == 1: # 2 == 1
    print("SUN")
elif day == 2: # 2 == 2 , true
    print("MON")
elif day == 3:
    print("TUE")
elif day == 4:
    print("WED")
elif day == 5:
    print("THRU")
elif day == 6:
    print("FRI")
elif day == 7:
    print("SAT")
else:
    print("Invalid")

    