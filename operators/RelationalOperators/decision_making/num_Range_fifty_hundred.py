# W.A.P TO CHECK THE NUMBER IS IN RANGE OF 50 TO 100

number = int(input("Enter the Number: ")) # number =60
if number>=50 and number<=100: # 60>=50 ,true and 60<=100 , true ,True and True= true
    print("Number is in the range of 50 to 100") # true
else:
    print("Number is not in the range of 50 to 100") 
