# W.A.P. to calculate percentage of marks
# Equation of finding percentage 
# Percentage = Marks Obtained / Total Marks * 100

maths = 60
english = 50
chemistry = 80
physics = 70
hindi = 65
total_marks = 500
marks_obtained = (maths+english+chemistry+physics+hindi)
percentage = marks_obtained/total_marks*100
print(percentage)
